    private void getData() {
        try{
            Connection conn = koneksi.getKoneksi(); 
            Statement stm = conn.createStatement();
            ResultSet result = stm.executeQuery("select * from tb_crud");
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            //reset data tabel
            model.setRowCount(0);
            
            while(result.next()){
                String id = result.getString("id");
                String nama = result.getString("nama");
                String ktp = result.getString("ktp");
                String jabatan = result.getString("jabatan");
                String nohp = result.getString("nohp");
                String alamat = result.getString("alamat");
                
                model.addRow(new Object[]{id, nama, ktp, jabatan, nohp, alamat});
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormKaryawan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
