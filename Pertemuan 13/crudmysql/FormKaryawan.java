    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //TOMBOL SAVE
        if(databaru) {
            try {
                String sql = "INSERT INTO tb_crud VALUES ('%s', '%s', '%s', '%s', '%s', '%s')";
                
                sql = String.format(
                        sql,
                        jTextField1.getText(),
                        jTextField2.getText(),
                        jTextField3.getText(),
                        jTextField4.getText(),
                        jTextField5.getText(),
                        jTextField6.getText()
                );
                
                Connection conn = koneksi.getKoneksi();
                PreparedStatement pst = conn.prepareStatement(sql);
                
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Berhasil disimpan");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal disimpan" + ex);
            }
        } else {
          try {
                String sql = "UPDATE tb_crud SET nama='%s', ktp='%s', jabatan='%s', nohp='%s', alamat='%s' WHERE id='%s'";
                
                sql = String.format(
                        sql,
                        jTextField2.getText(),
                        jTextField3.getText(),
                        jTextField4.getText(),
                        jTextField5.getText(),
                        jTextField6.getText(),
                        jTextField1.getText()
                );
                
                Connection conn = koneksi.getKoneksi();
                PreparedStatement pst = conn.prepareStatement(sql);
                
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Berhasil diedit");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal diedit" + ex);
            }  
        }
        
        getData();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try{
        databaru = false;
        
        int row = jTable1.getSelectedRow();
        String id_karyawan = jTable1.getModel().getValueAt(row, 0).toString();
        
        Connection conn = koneksi.getKoneksi();
        Statement stm = conn.createStatement();
        ResultSet sql = stm.executeQuery("SELECT * FROM tb_crud WHERE id='" + id_karyawan + "'");
        
        if (sql.next()) {
          jTextField1.setText(sql.getString("id")); 
          jTextField2.setText(sql.getString("nama")); 
          jTextField3.setText(sql.getString("ktp")); 
          jTextField4.setText(sql.getString("jabatan")); 
          jTextField5.setText(sql.getString("nohp")); 
          jTextField6.setText(sql.getString("alamat")); 
        }
    } catch (SQLException ex) {
        Logger.getLogger(FormKaryawan.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jTable1MouseClicked

